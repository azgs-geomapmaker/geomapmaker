• Conglomerate — Sedimentary rock, composed predominantly of particles or “clasts” derived by erosion and 
weathering of preexisting rock; contains more than 30 percent gravel-sized clasts.
• Sandstone — Sedimentary rock, composed predominantly of particles or “clasts” derived by erosion and weather-
ing of preexisting rock; consists mostly of sand-sized particles, with or without fine-grained matrix of silt or clay.
• Mostly sandstone — Mostly sandstone, interbedded with other sedimentary rocks that locally may include con-
