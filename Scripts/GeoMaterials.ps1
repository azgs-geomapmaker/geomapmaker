• Coarse-grained, mafic-composition intrusive igneous rock — Rock that solidified from molten or partly 
molten material (magma) at some depth beneath Earth’s surface, thereby cooling slowly enough for mineral 
crystals to grow large enough to be visible to naked eye. Composed mostly of feldspar and dark-colored minerals. 
