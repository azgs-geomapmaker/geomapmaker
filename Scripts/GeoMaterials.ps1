shallow depth beneath Earth’s surface, thereby cooling quickly. Generally fine grained but may contain large min-
eral crystals (phenocrysts). Mostly found as tabular dikes or sills.
• Fine-grained, felsic-composition intrusive igneous rock — Rock that solidified from molten or partly molten 
material (magma) at shallow depth beneath Earth’s surface, thereby cooling quickly. Generally fine grained but 
may contain large mineral crystals (phenocrysts). Mostly found as tabular dikes or sills. Composed mostly of 
light-colored minerals. Includes rhyolitic, dacitic, trachytic, and latitic rock.
• Fine-grained, intermediate-composition intrusive igneous rock — Rock that solidified from molten or partly 
molten material (magma) at shallow depth beneath Earth’s surface, thereby cooling quickly. Generally fine 
grained but may contain large mineral crystals (phenocrysts). Mostly found as tabular dikes or sills. Intermediate 
in color and in mineral composition (between felsic and mafic igneous rock). Includes andesitic rock.
• Fine-grained, mafic-composition intrusive igneous rock — Rock that solidified from molten or partly molten 
material (magma) at shallow depth beneath Earth’s surface, thereby cooling quickly. Generally fine grained but 
may contain large mineral crystals (phenocrysts). Mostly found as tabular dikes or sills. Composed mostly of 
dark-colored minerals. Includes basaltic rock.
• Exotic-composition intrusive igneous rock — Rock that solidified from molten or partly molten material (magma) 
below Earth’s surface that has exotic mineralogical, textural, or field setting characteristics. Typically dark colored 
with abundant phenocrysts. Includes kimberlite, lamprophyre, lamproite, and foiditic rocks.
•  Igneous and metamorphic rock — Consists of coarse-grained intrusive igneous rock and generally medium- to high-
grade metamorphic rock.
•  Metamorphic rock — Rock derived from preexisting rocks and altered by essentially solid-state mineralogical, chemi-
cal, or structural changes, in response to marked changes in temperature, pressure, deformation, and (or) chemical envi-
ronment, generally at depth in Earth’s crust.
• Regional metamorphic rock, of unspecified origin — Rock derived from preexisting rocks and altered by essentially 
solid-state mineralogical, chemical, or structural changes, in response to marked regional changes in temperature, 
pressure, deformation, and (or) chemical environment, generally at depth in Earth’s crust. Origin of preexisting rock is 
mixed (for example, igneous and sedimentary) or is not known. 
• Lower-grade metamorphic rock, of unspecified origin — Rock derived from preexisting rocks and altered by essentially 
solid-state mineralogical, chemical, or structural changes, in response to relatively mild regional changes in temperature, 
pressure, deformation, and (or) chemical environment, generally at depth in Earth’s crust. Origin of preexisting rock is 
mixed (for example, igneous and sedimentary) or is not known. Includes slate and phyllite.
