unspecified) proportions. 
• Mudstone — Sedimentary rock, composed predominantly of particles or “clasts” derived by erosion and weathering of 
preexisting rock; consists mostly of mud (that is, silt- and clay-sized particles). Includes shale and siltstone.
• Mostly mudstone — Mostly mudstone, interbedded with other sedimentary rocks that locally may include 
coarser grained clastic rocks (sandstone, conglomerate), carbonates, and (or) coal.
• Carbonate rock — Sedimentary rock, consisting chiefly of carbonate minerals such as limestone or dolomite. 
• Limestone — Carbonate sedimentary rock, consisting chiefly of calcite.
• Dolomite — Carbonate sedimentary rock, consisting chiefly of dolomite. Although “dolostone” is the proper 
analog to “limestone”, it has not often been applied to dolomitic units.
• Mostly carbonate rock — Mostly carbonate rock, interbedded with other sedimentary rock types.
• Chert — Sedimentary rock, composed chiefly of microcrystalline or cryptocrystalline quartz.
• Evaporitic rock — Sedimentary rock, composed primarily of minerals produced by evaporation of saline solution. 
Examples include gypsum, anhydrite, other diverse sulfates, halite (rock salt), primary dolomite, and rocks composed of 
various nitrates and borates.
• Iron-rich sedimentary rock — Sedimentary rock, in which at least half (by volume) of observed minerals are iron 
bearing (hematite, magnetite, limonite group minerals, siderite, iron sulfides).
• Coal and lignite — Organic-rich sedimentary rock, formed from compaction and alteration of plant remains. Coal 
is consolidated, harder, black rock. Lignite is semiconsolidated, brown to black, earthy material that may contain 
large particles of recognizable plant parts and tends to crack upon drying.
•  Sedimentary and extrusive igneous material — Either (1) sedimentary rock and (or) unconsolidated material (sedi-
ment) and extrusive igneous material (volcanic rock and [or] sediment) or (2) volcanic rock and (or) sediment and such 
material after erosion and redeposition.
•  Igneous rock — Rock and fragmental material that solidified from molten or partly molten material (magma). 
• Extrusive igneous material — Molten material that was erupted onto Earth’s surface, fusing into rock or remaining as 
unconsolidated particles. Includes pyroclastic flows, air-fall tephra, lava flows, and volcanic mass flows.
• Volcaniclastic (fragmental) material — Rock and unconsolidated material consisting of particles or “clasts” that 
were formed by volcanic explosion or aerial expulsion from volcanic vent.
• Pyroclastic flows — Hot ash, pumice, and rock fragments erupted from volcano or caldera. Material moves 
downslope commonly in chaotic flows; once deposited, hot fragments may compact under their own weight and 
weld together.
• Felsic-composition pyroclastic flows — Hot ash, pumice, and rock fragments erupted from volcano or 
caldera. Material moves downslope commonly in chaotic flows; once deposited, hot fragments may compact 
under their own weight and weld together. Because of their high-silica content and resulting high viscosity, 
parental magmas tend to erupt explosively. Includes rhyolite, dacite, trachyte, latite; rocks are commonly light-
colored.
• Intermediate-composition pyroclastic flows — Hot ash, pumice, and rock fragments erupted from volcano. 
Material moves downslope commonly in chaotic flows; once deposited, hot fragments may compact under 
their own weight and weld together. Parental magma commonly erupts from stratovolcanoes as thick lava 
flows but also can generate strong explosive eruptions to form pyroclastic flows. Includes rocks that are, in 
color and mineral composition, intermediate between felsic and mafic rocks (for example, andesite).
• Mafic-composition pyroclastic flows — Hot ash, pumice, and rock fragments erupted from volcano. Material 
moves downslope commonly in chaotic flows; once deposited, hot fragments may compact under their own 
weight and weld together. Because of their low silica content and resulting low viscosity, parental magmas tend 
to erupt gently as lava flows rather than more forcefully as pyroclastic flows. Includes basalt; rocks are com-
monly dark-colored. 
