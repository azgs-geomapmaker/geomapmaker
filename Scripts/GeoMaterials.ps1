quartz). Includes granitic, syenitic, and monzonitic rock.
• Coarse-grained, intermediate-composition intrusive igneous rock — Rock that solidified from molten or 
partly molten material (magma) at some depth beneath Earth’s surface, thereby cooling slowly enough for min-
