altered by essentially solid-state mineralogical, chemical, or structural changes, in response to relatively intense regional 
changes in temperature, pressure, deformation, and (or) chemical environment, generally at depth in Earth’s crust. Origin of 
preexisting rock is mixed (for example, igneous and sedimentary) or is not known. Includes amphibolite, granulite, schist, 
and gneiss.
• Contact-metamorphic rock — Altered rock that originated by local processes of thermal metamorphism, genetically 
related to intrusion and extrusion of magmas and taking place at or near contact with body of igneous rock. Metamorphic 
changes are affected by heat and fluids emanating from magma and by some deformation because of emplacement of 
igneous mass.
• Deformation-related metamorphic rock — Rock derived from preexisting rocks by essentially solid-state mineralogi-
cal, chemical, or structural changes in response to strong deformation, commonly in association with marked changes in 
temperature, pressure, and (or) chemical environment. Generally forms in narrow, planar zones of local deformation (for 
example, along faults); characterized by foliation or alignment of mineral grains. Includes mylonite and cataclasite.
• Metasedimentary rock — Rock derived from preexisting sedimentary rocks and altered by essentially solid-state 
mineralogical, chemical, or structural changes, in response to marked changes in temperature, pressure, deformation, 
and (or) chemical environment, generally at depth in Earth’s crust.
• Slate and phyllite, of sedimentary-rock origin — Fine-grained rock derived from preexisting sedimentary rocks 
and altered by essentially solid-state mineralogical, chemical, or structural changes, in response to marked changes 
in temperature, pressure, deformation, and (or) chemical environment, generally at depth in Earth’s crust. Includes 
phyllite and slate (compact, fine-grained rock that possesses strong cleavage and, hence, can be split into slabs and 
thin plates). Mostly formed from fine-grained material such as mudstone.
• Schist and gneiss, of sedimentary-rock origin — Foliated rock derived from preexisting sedimentary rocks by essen-
tially solid-state mineralogical, chemical, or structural changes in response to marked changes in temperature, pressure, 
deformation, and (or) chemical environment, generally at depth in Earth’s crust. Includes schist (characterized by such 
strong foliation or alignment of minerals that it readily splits into flakes or slabs) and gneiss (characterized by alternat-
ing, irregular bands of different mineral composition). Mostly formed from fine-grained material such as mudstone.
• Meta-carbonate rock — Rock derived from preexisting carbonate sedimentary rocks and altered by essentially 
solid-state mineralogical, chemical, or structural changes, in response to marked changes in temperature, pressure, 
deformation, and (or) chemical environment, generally at depth in Earth’s crust. Characterized by recrystallization 
of carbonate minerals in source rock. Includes marble (for which preexisting rock was dominantly limestone or 
other rock composed of calcite), dolomitic marble, meta-dolostone, and meta-dolomite (for which preexisting rock 
contained appreciable amount of magnesium).
• Quartzite — Rock derived from preexisting quartz-rich sedimentary rocks (commonly sandstone) and altered by 
essentially solid-state mineralogical, chemical, or structural changes, in response to marked changes in temperature, 
pressure, shear stress, and (or) chemical environment, generally at depth in Earth’s crust. 
• Metaigneous rock — Rock derived from preexisting igneous rocks and altered by essentially solid-state, mineralogical, 
chemical, or structural changes, in response to marked changes in temperature, pressure, shear stress, and (or) chemical 
environment, generally at depth in Earth’s crust. 
• Meta-ultramafic rock — Rock derived from preexisting ultramafic rocks by essentially solid-state, mineralogi-
cal, chemical, or structural changes, in response to marked changes in temperature, pressure, deformation, and (or) 
chemical environment, generally at depth in Earth’s crust. Composed mostly of magnesium-bearing minerals (for 
example, serpentine, talc, magnesite).
• Meta-mafic rock — Rock derived from preexisting mafic rocks by essentially solid-state, mineralogical, chemi-
cal, or structural changes, in response to marked changes in temperature, pressure, deformation, and (or) chemical 
environment, generally at depth in Earth’s crust. Composed mostly of iron- and magnesium-bearing, dark-colored 
and (or) green minerals. Includes greenstone, amphibolite, and metagabbro.
• Meta-felsic and intermediate rock — Rock derived from preexisting felsic and intermediate-composition rocks 
by essentially solid-state, mineralogical, chemical, or structural changes, in response to marked changes in tempera-
ture, pressure, deformation, and (or) chemical environment, generally at depth in Earth’s crust. Composed mostly of 
light-colored minerals; relatively enriched in silica. Includes metagranite, metadiorite, and meta-andesite.
• Meta-volcaniclastic rock — Rock derived from preexisting volcaniclastic rocks by essentially solid-state, miner-
alogical, chemical, or structural changes, in response to marked changes in temperature, pressure, deformation, and 
(or) chemical environment, generally at depth in Earth’s crust. Composed of deformed but recognizable particles or 
clasts of volcanic explosive material.
