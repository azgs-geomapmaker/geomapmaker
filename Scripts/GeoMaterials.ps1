increasing distance from volcano, average size of individual rock particles and thickness of resulting deposit decrease. 
Because of their low silica content and resulting low viscosity, parental magmas tend to erupt gently as lava flows, and 
so these deposits are uncommon. Includes basalt; rocks are commonly dark-colored. 
• Lava flows — Lateral, surficial outpourings of molten lava from vent or fissure; also, solidified bodies of rock that 
form when they cool. Composed generally of fine-grained, dark-colored rocks (for example, basalt), which tend to 
form extensive sheets that have generally low relief, except in vent areas where cinder cones or shield volcanoes 
may form. Includes basaltic shield volcanoes, which may become very large (for example, Hawaii).
• Felsic-composition lava flows — Lateral, surficial outpourings of molten lava from vent or fissure; also, solidi-
fied bodies of rock that form when they cool. Because of their high silica content and resulting high viscosity, 
parental magmas tend to erupt explosively, and so these deposits are uncommon. Includes fine-grained, light-
colored rock with rhyolitic, dacitic, trachytic, and latitic composition.
• Intermediate-composition lava flows — Lateral, surficial outpourings of molten lava from vent or fissure; also, 
solidified bodies of rock that form when they cool. Parental magma commonly erupts from stratovolcanoes as 
thick lava flows. Includes rocks that are, in color and in mineral composition, intermediate between felsic and 
mafic rocks (for example, andesite).
• Mafic-composition lava flows — Lateral, surficial outpourings of molten lava from vent or fissure; also, solidi-
fied bodies of rock that form when they cool. Low-silica parental magmas have low viscosity and tend to form 
extensive sheets that have generally low relief. Includes basaltic shield volcanoes, which may become very large 
(for example, in Hawaii). Composed of fine-grained, dark rocks, including basaltic.
