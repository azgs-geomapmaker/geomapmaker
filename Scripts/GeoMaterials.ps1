• Peat and muck — Unconsolidated material, principally composed of plant remains, with lesser amounts of fine-grained 
clastic sediment. Deposited in water-saturated environment such as swamp, marsh, or bog. With lithification, such material 
becomes coal.
• Sedimentary rock — Consolidated material (rock) composed of particles transported and deposited by gravity, air, 
water, or ice, or accumulated by other natural agents operating at Earth’s surface, such as chemical precipitation or 
secretion by organisms. Does not here include sedimentary material directly deposited as result of volcanic activity.
• Clastic sedimentary rock — Sedimentary rock, composed predominantly of particles or “clasts” derived by ero-
