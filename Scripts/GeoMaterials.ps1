deposited at some distance from volcano is known as volcanic ash.
• Felsic-composition air-fall tephra — Fragments of volcanic rock and lava, of various sizes, carried into air by 
explosions and by hot gases in eruption columns or lava fountains; known as tephra. As tephra falls to ground, 
